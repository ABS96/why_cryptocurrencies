If we're going to talk about cryptocurrencies we need to know what they are. Otherwise how can we tell what value---if any---they have? And I think the best description of what a cryptocurrency is can be found in the title of the original white paper: ◊link[wp]{Bitcoin: A Peer-to-Peer Electronic Cash System}. It's like cash, but in digital form.◊mn{read-wp}

In this section we'll begin by looking at the properties cryptocurrencies have, which lays the foundation for the use-cases described in the book, and we'll take a closer look at how cryptocurrencies work. Technical understanding isn't required too see their usefulness, but it helps us navigate the cryptocurrency space and to see through misleading information.

I'll try to backup my claim that cryptocurrencies are like cash by discussing what money is, the properties of money and if it's fair to classify cryptocurrencies as money. Not only will I say that cryptocurrencies are money, but that they're potentially the best money we've ever seen, although they're held back by volatility and low adoption.

